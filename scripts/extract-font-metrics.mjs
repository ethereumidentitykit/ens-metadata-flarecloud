#!/usr/bin/env node
import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";
import opentype from "opentype.js";

const here = dirname(fileURLToPath(import.meta.url));
const FONT_PATH = resolve(here, "../src/fonts/Satoshi-Bold.ttf");
const OUT_PATH  = resolve(here, "../src/lib/fontMetrics.ts");
const FONT_SIZE = 20;

const RANGES = [
	[0x20, 0x7e],
	[0xa0, 0x017f],
	[0x0250, 0x02af],
	[0x02b0, 0x02ff],
	[0x0370, 0x03ff],
];

const SKIP_RANGES = [
	[0x30, 0x39],
];

function shouldSkip(cp) {
	for (const [lo, hi] of SKIP_RANGES) {
		if (cp >= lo && cp <= hi) return true;
	}
	return false;
}

const ttf = await readFile(FONT_PATH);
const font = opentype.parse(ttf.buffer.slice(ttf.byteOffset, ttf.byteOffset + ttf.byteLength));

function advanceAtFontSize(char) {
	const glyph = font.charToGlyph(char);
	if (!glyph || glyph.name === ".notdef") return null;
	const advance = font.getAdvanceWidth(char, FONT_SIZE);
	return advance && Number.isFinite(advance) ? advance : null;
}

const entries = new Map();
for (const [lo, hi] of RANGES) {
	for (let cp = lo; cp <= hi; cp++) {
		if (shouldSkip(cp)) continue;
		const adv = advanceAtFontSize(String.fromCodePoint(cp));
		if (adv === null) continue;
		entries.set(cp, adv);
	}
}

const sorted = [...entries.entries()].sort((a, b) => a[0] - b[0]);
const lines = sorted.map(
	([cp, adv]) =>
		`\t${cp.toString().padStart(6)}: ${adv.toFixed(4)}, // U+${cp.toString(16).toUpperCase().padStart(4, "0")} ${
			cp >= 0x20 && cp < 0x7f ? `'${String.fromCodePoint(cp)}'` : ""
		}`,
);

const fontFile = `Satoshi-Bold.ttf`;
const out = `// Auto-generated by scripts/extract-font-metrics.mjs from src/fonts/${fontFile}.
// DO NOT EDIT. Re-run \`npm run extract-font-metrics\` if the font file changes.
//
// Glyph horizontal advances at ${FONT_SIZE}px Satoshi-Bold, in CSS px (truncated
// to 4 decimal places). Used by getFontSize in services/nameImage.ts to size
// the rendered name to fit the canvas. Digits 0-9 are intentionally omitted —
// upstream's node-canvas measureText reports them ~2x wider than the TTF's
// advances so we override with a hand-tuned constant in estimateAdvance.

export const SATOSHI_BOLD_ADVANCE_AT_20PX: Readonly<Record<number, number>> = Object.freeze({
${lines.join("\n")}
});
`;

await writeFile(OUT_PATH, out);
console.log(`wrote ${entries.size} entries → ${OUT_PATH}`);
